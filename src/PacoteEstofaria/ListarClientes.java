    private void preencheTabela(ArrayList<Cliente> lista){
      
        DefaultTableModel tabela = (DefaultTableModel) tabelaClientesListados.getModel();
         
        while(tabelaClientesListados.getRowCount()>0){
            tabela.removeRow(0);
        } 
        
         for(Cliente X : lista){
             String[] dataFinal; 
             dataFinal = X.getDataAdd().replace(" de ", "/").split("--");
             tabela.addRow(new String[] {X.getNome(),X.getID(),X.getEndereco(),X.getBairro(),X.getTelefone(),X.getCelular(),String.valueOf(X.getServicos().size()),dataFinal[0]});
         }
         
    }
