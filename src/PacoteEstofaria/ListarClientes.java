    private void ordenaNomeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ordenaNomeActionPerformed
        listaOrdenada = new ArrayList();
        listaAchadaIndexes = new ArrayList();
        ordenaNome.setSelected(true);
        ordenaEndereco.setSelected(false);
        ordenaBairro.setSelected(false);
        ordenaData.setSelected(false);
        
        for(Cliente X:lista){
            listaOrdenada.add(X);
        }
        
        listaOrdenada.sort(new Comparator<Cliente>()
                 {
                     public int compare(Cliente p1, Cliente p2)
                     {
                         return p1.getNome().trim().toLowerCase().compareTo(p2.getNome().trim().toLowerCase());
                     }        
                 });
        
        preencheTabela(listaOrdenada);
        
    }//GEN-LAST:event_ordenaNomeActionPerformed

