    //------------------------------------------------------------------------------Lista de opções menu principal.
    
    public void atualizaUltimosClientes(){
        ArrayList<Cliente> ListaClientes = this.clientes;
                
                int QtndLinhas = ListaClientes.size();
                
                DefaultListModel model = new DefaultListModel();
               
                if(QtndLinhas < 1)
                model.addElement("  ---------------------------");
                else
                model.addElement((ListaClientes.get(QtndLinhas-1)).getNome());
                
               if(QtndLinhas < 2)
                model.addElement("  ---------------------------");
                else
                model.addElement((ListaClientes.get(QtndLinhas-2)).getNome());
                
               if(QtndLinhas < 3)
                model.addElement("  ---------------------------");
                else
                model.addElement((ListaClientes.get(QtndLinhas-3)).getNome());
                
               if(QtndLinhas < 4)
                model.addElement("  ---------------------------");
                else
                model.addElement((ListaClientes.get(QtndLinhas-4)).getNome());
                
               if(QtndLinhas < 5)
                model.addElement("  ---------------------------");
                else
                model.addElement((ListaClientes.get(QtndLinhas-5)).getNome());
                
               listaClientes.setModel(model);
    }
