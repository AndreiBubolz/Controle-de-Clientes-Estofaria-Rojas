    private void botaoExpandirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoExpandirActionPerformed
        if(!botaoExpandir.isSelected()){
            int y= this.getSize().height;
            botaoExpandir.setText("Mostrar mais...");

            this.setSize(this.getSize().width, y-200);
            this.setLocation(this.getLocation().x,this.getLocation().y+100);
        }else{
            int y= this.getSize().height;
            botaoExpandir.setText("Mostrar menos...");
            this.setLocation(this.getLocation().x, this.getLocation().y-200);
            
            if(this.getLocation().y < 0)
                this.setLocation(this.getLocation().x, 20);
            
            this.setSize(this.getSize().width, y+200);
        }
    }//GEN-LAST:event_botaoExpandirActionPerformed

